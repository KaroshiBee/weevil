module Render_output = struct
  type msg = {
        modstr: string;
        tystr: string;
        ctor: string;
        ctorstr: string;
        genstr: string;
        encstr: string;

      }
  type mlmli = {ml: string; mli:string;}

  type t = [
    | `Message of msg
    | `MlMli of mlmli
  ]

  let make_msg ?(tystr="") ?(ctor="") ?(ctorstr="") ?(genstr="") ?(encstr="") modstr =
    `Message {modstr; tystr; ctor; ctorstr; genstr; encstr}

  let make_mlmli ml mli =
    `MlMli {ml; mli}

end


